PHP tiene distintos operadores, pero los más utilizados son:
Aritméticos, Lógicos, de Comparación, de Asignación

En conclusión, saber utilizar los distintos operadores es fundamental para evaluar valores,
hacer comparaciones y agrupar elementos. Para que puedas dominar este tema, en las siguientes clases conocerás la sintaxis,
el funcionamiento y los modos de uso de cada operador.


Operadores Aritmeticos
+ - * / %

Operadores lógicos
Los operadores lógicos:
nos permiten realizar una comparación entre dos valores de tipo boolean
Así mismo, se emplean generalmente con expresiones que devuelven valores lógicos: true o false

Operador lógico:
Ejempo        Nombre      Resultado
$a and $b     And(y)       true si tanto $a como $b son true


Operadores de comparación:
Los operadores de comparación se utilizan para comparar dos valores, ya sean números o cadenas

El operador de comparación igual (==) compara si dos valores son iguales. En este caso, devuelve true, en caso contrario, false. El operador (===) compara si los elementos son idénticos. Pero, si deseamos saber si los elementos a comparar son distintos, usamos (!=).

El operador no idéntico (!==) es lo contrario al operador idéntico. Es decir, devuelve true si los dos valores diferentes en valor y en tipo de dato. El operador de comparación mayor qué (>) devuelve true si el valor de la izquierda es mayor que el de la derecha.

El operador (>=) se emplea si un elemento es mayor o igual que otro. Utilizamos menor qué (<) si el valor de la izquierda es menor que el de la derecha.

Y por último, el operador (<=) lo empleamos cuando un elemento es menor o igual que otro.


Operadores de asignación:
Los operadores de asignación:
son aquellos que se utilizan para asignar un valor a una variable.
Aunque el operador de asignación más conocido es el signo igual (=), existen otros operadores que son una derivación de este signo.

Puede observar que el operador += en x+=1 es equivalente a x=x+1. Así mismo, (-=) es equivalente a restar y asignar el resultado a la variable inicial, es muy usado para decrementar el valor de una variable.

El operador (*=) equivale a multiplicar una variable por otra y almacenar el resultado en la primera. Es decir, x*=2 equivale a x=x*2. Por el contrario, (/=) equivale a dividir una variable por otra y almacenar el resultado en la primera, es decir, x/=2 equivale a x=x/2.

Por último, el operador (%=) equivale a hacer el módulo de la división de dos variables y almacenar su resultado en la primera.
